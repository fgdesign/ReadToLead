<script type="text/javascript"> 
		var anchors = ['firstPanel', 'secondPanel'];
    	var myFullpage = new fullpage('#fullpage', {
        anchors: anchors,
        sectionsColor: ['#fff', '#f5f5f5'],
        fixedElements: '#header',
        scrollOverflow: true,
        responsiveWidth: 992,						//width were fullpage scroll is disabled//
        afterResponsive: function(isResponsive){

        },
        
        afterLoad: function(origin, destination, direction){
			if(origin){
				switch (origin.anchor) {
					case anchors[0]:
						slide1Out();
						break;
					case anchors[1]:
						slide2Out();
						
						
				}
			}
			if(destination){
				switch (destination.anchor) {
					case anchors[0]:
						slide1In();
						break;
					case anchors[1]:
						slide2In();
					
						
				}
			}			
		}
        
        

  
    });
    
    
    
function slide1In(){
	console.log("Section firstPage animates in");
	$('.maggie').addClass('animated bounceInUp delay-5'); 	//will break if the class doesn't exist//
    $('.maggie').removeClass('invisible');
    $('.clive').addClass('animated bounceInUp delay-8'); 	
    $('.clive').removeClass('invisible');
    $('.brendon').addClass('animated bounceInRight delay-7'); 	
    $('.brendon').removeClass('invisible');
    
}
function slide1Out(){
	console.log("Section firstPage animates out");
  	$('.maggie').removeClass('animated bounceInUp delay-5'); 	
    $('.maggie').addClass('invisible');
    $('.clive').removeClass('animated bounceInUp delay-8'); 	
    $('.clive').addClass('invisible');
    $('.brendon').removeClass('animated bounceInRight delay-7'); 	
    $('.brendon').addClass('invisible');
   
}

function slide2In(){
	console.log("Section thirdPage animates in");
	$('.atomic-dog').addClass('animated fadeInUp delay-3'); 	
    $('.atomic-dog').removeClass('invisible');
    $('.dumbell').addClass('animated fadeIn delay-2'); 	
    $('.dumbell').removeClass('invisible');
    $('.open-book').addClass('animated fadeIn delay-4'); 	
    $('.open-book').removeClass('invisible');
    $('.screen').addClass('animated fadeIn delay-6'); 	
    $('.screen').removeClass('invisible');
    
  	
}
function slide2Out(){
	console.log("Section thirdPage animates out");
	$('.atomic-dog').addClass('invisible');
    $('.atomic-dog').removeClass('animated fadeInUp delay-3');
    $('.dumbell').removeClass('animated fadeIn delay-2'); 	
    $('.dumbell').addClass('invisible');
    $('.open-book').removeClass('animated fadeIn delay-4'); 	
    $('.open-book').addClass('invisible');
    $('.screen').removeClass('animated fadeIn delay-6'); 	
    $('.screen').addClass('invisible');
    
 	
}

